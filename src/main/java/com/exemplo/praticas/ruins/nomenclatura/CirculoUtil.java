package com.exemplo.praticas.ruins.nomenclatura;

public class CirculoUtil {

	private CirculoUtil() {

	}

	/*
	 * Nome do método deve representar o objetivo do método.
	 * Nomes dos parâmetros e variáveis devem ser claros.
	 * O valor do PI deve ser uma constante.
	 */
	public static double r(final double r) {
		return 3.14 * r * r;
	}

}
